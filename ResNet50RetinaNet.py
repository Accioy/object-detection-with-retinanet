# ## Run detection on example

# In[ ]:


# load image
imagefile="000682.jpg"
image_name=os.path.join("F:\\yan\\keras-retinanet-master\\VOC2007\\JPEGImages",imagefile)
# image_name="vitest2.jpg"
image = read_image_bgr(os.path.join(ROOT_DIR, image_name))

# copy to draw on
draw = image.copy()
draw = cv2.cvtColor(draw, cv2.COLOR_BGR2RGB)

# preprocess image for network
image = preprocess_image(image)
image, scale = resize_image(image,min_side=500, max_side=600)

# process image
start = time.time()
boxes, scores, labels = model.predict_on_batch(np.expand_dims(image, axis=0))
print("processing time: ", time.time() - start)

# correct for image scale
boxes /= scale

# visualize detections
# boxes的shape:(1,300,4),即1张图，300个框，4个坐标，scores和labels的shape是(1,300)
# 此处boxes已经过筛选，实际上仅有少数有效boxes，其余boxes的scores为-1
for box, score, label in zip(boxes[0], scores[0], labels[0]):
    # scores are sorted so we can break
    # 如果最高的score小于0.5，说明所有的score小于0.5
    if score < 0.5:
        break
        
    color = label_color(label)
    
    b = box.astype(int)
    draw_box(draw, b, color=color)
    
    caption = "{} {:.3f}".format(labels_to_names[label], score)
    draw_caption(draw, b, caption)
    
plt.figure(figsize=(15, 15))
plt.axis('off')
plt.imshow(draw)
